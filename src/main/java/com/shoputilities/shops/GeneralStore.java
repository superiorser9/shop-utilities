	public static GeneralStore getData(String shopName)
	{
		for (GeneralStore generalStore : GeneralStore.values())
		{
			if (generalStore.shopName.equalsIgnoreCase(shopName))
			{
				return generalStore;
			}
		}
		return null;
	}

	public static boolean cannotSellEverything(String shopName)
	{
		return shopName.equalsIgnoreCase("Bandit Bargains") || shopName.equalsIgnoreCase("Bedabin Village Bartering") || shopName.equalsIgnoreCase("Shantay Pass Shop");
	}

